    public boolean validar_campos(){
        boolean flag = false;
        if(txt_year.getText().length() == 4 && txt_ap_paterno.getText().length()>0 && txt_ap_paterno.getText().length()<51 && txt_ap_materno.getText().length()>0 
                && txt_ap_materno.getText().length()<51 && txt_nombres.getText().length()>0 && txt_nombres.getText().length()<101 && (txt_dni.getText().length()==0 ||txt_dni.getText().length()==8)
                && txt_cod_modular.getText().length()<21 && txt_cargo.getText().length()<61 && txt_nivel_rem.getText().length()<21 && txt_horas.getText().length()<4
                && txt_cod_planilla.getText().length()<31 && txt_establecimiento.getText().length()<121){
            flag = true;
        }        
        return flag;
    }
