    public void verif(KeyEvent evt, JTextField txt){
        char validar = evt.getKeyChar();
        if((validar<'0' || validar>'9') && txt.getText().contains(".") && (validar!=(char)KeyEvent.VK_BACK_SPACE)){
            evt.consume();
        }else if((validar<'0' || validar>'9')&&(validar!='.')&&(validar!=(char)KeyEvent.VK_BACK_SPACE)){
            evt.consume();
        }
    }
