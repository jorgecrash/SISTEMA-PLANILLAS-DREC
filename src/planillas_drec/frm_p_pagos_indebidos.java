    private void btn_ENVIAR_ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_ENVIAR_ActionPerformed
        int i = jTable1.getRowCount(); 
        if(i>0){
            float suma_indebidos_pe = 0;
            for (int j = 0; j < i; j++) {
                campos_pagos_p.add(jTable1.getValueAt(j,0).toString());
                valores_pagos_p.add(Float.parseFloat(jTable1.getValueAt(j,1).toString()));
                suma_indebidos_pe+=Float.parseFloat(jTable1.getValueAt(j,1).toString());
            }
            frm_planilla_v2.txt_valor_p_.setText(Float.toString(suma_indebidos_pe));
            this.dispose();
        }
    }//GEN-LAST:event_btn_ENVIAR_ActionPerformed
    
