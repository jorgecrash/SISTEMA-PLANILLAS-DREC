    private void txt_valorKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txt_valorKeyTyped
        char validar = evt.getKeyChar();
        if((validar<'0' || validar>'9') && txt_valor.getText().contains(".") && (validar!=(char)KeyEvent.VK_BACK_SPACE)){
            evt.consume();
        }else if((validar<'0' || validar>'9')&&(validar!='.')&&(validar!=(char)KeyEvent.VK_BACK_SPACE)){
            evt.consume();
        }
    }//GEN-LAST:event_txt_valorKeyTyped

    private void btn_add_ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_add_ActionPerformed
        boolean flag = validar_campos();
        if(flag){
            String pago_ind = combo_pagos.getSelectedItem().toString();
            boolean banderita = verificar_R_en_tabla(pago_ind);
            if(banderita){
                String valor = txt_valor.getText();
                String a [] = {pago_ind,valor};
                table.addRow(a);
                txt_valor.setText("");
            }
            else
            JOptionPane.showMessageDialog(null, "Ya agregó dicho pago indebido: "+pago_ind, "MENSAJE", JOptionPane.WARNING_MESSAGE);
        }
        else
        JOptionPane.showMessageDialog(null, "Complete todos los campos adecuadamente...", "MENSAJE", JOptionPane.WARNING_MESSAGE);
    }//GEN-LAST:event_btn_add_ActionPerformed
    public boolean validar_campos(){
        boolean flag = false;
        if(txt_valor.getText().length() > 0 && !txt_valor.getText().equals(".")){
            flag = true;
        }        
        return flag;
    }
    public boolean verificar_R_en_tabla(String R){
        boolean flag = true;
        int i = jTable1.getRowCount(); 
        for (int j = 0; j < i; j++) {
            if(R.equals(jTable1.getValueAt(j, 0).toString())){
                flag = false;
                j = i;
            }
        }
        return flag;
    }
