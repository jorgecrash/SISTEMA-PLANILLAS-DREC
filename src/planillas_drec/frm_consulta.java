    public void redirigir_form(String user) throws SQLException{
        String privilegio = c.tipo_user(user);
        if(privilegio.equals("planillero")){
            btn_consolidado.setVisible(false);
        }
        else if(privilegio.equals("consultor")){
            btn_planilla.setVisible(false);
            btn_consolidado.setVisible(false);
        }
    }    
    public String[] len(String a[]){
        for (int i = 0; i < a.length; i++) {
            a[i] = String.format("%-25s", a[i]);
        }
        return a;       
    }
    public void eliminar_null(ArrayList nombre_campos_hab_des, ArrayList valor_hab_des,
            ArrayList campos_aux, ArrayList valor_aux,int p_ini, int p_fin){
        int k=0;
        for (int i = p_ini; i < p_fin; i++) {
            if(valor_hab_des.get(i)!=(null)){
                campos_aux.add(nombre_campos_hab_des.get(k));
                valor_aux.add(valor_hab_des.get(i));                
            }
            k++;       
        } 
    }
