    private void btn_GENERAR_REPORTE_ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_GENERAR_REPORTE_ActionPerformed
        boolean flag = validar_campos();
        if(flag){
            String mes = combo_mes.getSelectedItem().toString();
            String año = txt_year.getText(); 
            String pat = txt_ap_paterno.getText().trim();
            String mat = txt_ap_materno.getText().trim();
            String name = txt_nombres.getText().trim();
            String arr[] = {mes,año,pat,mat,name};
            try {
                int contador = c.verificar_consulta(arr);
                if(contador > 0){
                    String campos [] = c.campo_nombre();
                    String campos_boleta[] = new String[13];
                    ArrayList<String> campos_haberes = new ArrayList();
                    ArrayList<String> campos_descuentos = new ArrayList();
                    ArrayList<String> campos_importes = new ArrayList();
                    int ind = 0;
                    while(ind<13){
                        campos_boleta[ind] = campos[ind];
                        ind++;
                    }
                    while(ind<16){
                        campos_importes.add(campos[ind]);
                        ind++;
                    }
                    ind++;
                    while(ind<67){
                        campos_haberes.add(campos[ind]);
                        ind++;
                    }
                    ind++;
                    while(ind<145){
                        campos_descuentos.add(campos[ind]);
                        ind++;
                    }
                    campos_boleta = len(campos_boleta);
                    ArrayList<String> boleta = new ArrayList();
                    ArrayList<String> importes = new ArrayList();
                    ArrayList<String> haberes = new ArrayList();
                    ArrayList<String> descuentos = new ArrayList();           
                    c.valores_de_la_consulta_individual(boleta,importes,haberes, descuentos, arr);
                    if(rbtn_txt.isSelected()){
                        //if(contador>1){
                        generar_boleta_txt(campos_boleta,campos_importes,campos_haberes,campos_descuentos,boleta,importes,haberes,descuentos);
                       // }
                       
                    }
                    else if(rbtn_pdf.isSelected()){
                        generar_boleta_pdf(campos_boleta,campos_importes,campos_haberes,campos_descuentos,boleta,importes,haberes,descuentos);                       
                    }
                }
                else
                    JOptionPane.showMessageDialog(null, "¡No existe registro con tales datos!", "MENSAJE", JOptionPane.WARNING_MESSAGE);
                      
            } catch (SQLException | HeadlessException e) {
                JOptionPane.showMessageDialog(null, "Algo salió mal..."+e);
            }
        }
        else
            JOptionPane.showMessageDialog(null, "Complete y/o seleccione todos los campos adecuadamente...", "MENSAJE", JOptionPane.WARNING_MESSAGE);
            
    }//GEN-LAST:event_btn_GENERAR_REPORTE_ActionPerformed
    public boolean validar_campos(){
        boolean flag = false;
        if(txt_year.getText().length() == 4 && txt_ap_paterno.getText().length() > 0 && txt_ap_materno.getText().length() > 0 && txt_nombres.getText().length() > 0 && (rbtn_txt.isSelected()|| rbtn_pdf.isSelected())){
            flag = true;
        }        
        return flag;
    }
